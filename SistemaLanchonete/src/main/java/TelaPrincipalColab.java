    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        DateTimeFormatter localDateFormatter = DateTimeFormatter.ofPattern("dd/MM/uuuu");
        DateTimeFormatter localHourFormatter = DateTimeFormatter.ofPattern("HH:mm");
        DefaultTableModel dtmPedidos = (DefaultTableModel) listPedidos.getModel();

        dtmPedidos.setRowCount(0);

        if (ProxyAdministrador.consultaCliente(cpfCliente.getText()) != null) {
            for (Pedido Pe : ProxyAdministrador.consultaCliente(cpfCliente.getText()).getPedidosCliente()) {
                int idPedido = Pe.getIdPedido();

                LocalDate dataPe = LocalDate.parse(Pe.getDataPedido());
                LocalTime horaPe = LocalTime.parse(Pe.getHoraPedido());
                String dataPedido = dataPe.format(localDateFormatter);
                String horaPedido = horaPe.format(localHourFormatter);
                float valorPedido = Pe.getValorTotalPedido();

                Object[] dados = {idPedido, dataPedido, horaPedido, valorPedido};
                dtmPedidos.addRow(dados);
            }

        } else {
            JOptionPane.showMessageDialog(null, "CPF inválido ou não cadastrado. Tente novamente.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void gerarExtratosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gerarExtratosActionPerformed
        DateTimeFormatter localDateFormatter = DateTimeFormatter.ofPattern("dd/MM/uuuu");
        DateTimeFormatter localHourFormatter = DateTimeFormatter.ofPattern("HH:mm");
        DefaultTableModel dtmExtPedidos = (DefaultTableModel) extratosPedidosTabela.getModel();

        dtmExtPedidos.setRowCount(0);

        for (Cliente cl : ProxyAdministrador.getClientes()) {
            for (Pedido pedido : cl.getPedidosCliente()) {
                LocalTime horaPedido = LocalTime.parse(pedido.getHoraPedido());
                LocalDate dataPedido = LocalDate.parse(pedido.getDataPedido());
                Object[] dados = {cl.getCPF(), cl.getNomePessoa() + " " + cl.getSobrenomePessoa(), pedido.getIdPedido(), pedido.getValorTotalPedido(),
                    dataPedido.format(localDateFormatter), horaPedido.format(localHourFormatter)};
                dtmExtPedidos.addRow(dados);
            }
        }


    }//GEN-LAST:event_gerarExtratosActionPerformed

