    private void gerarRelVendasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gerarRelVendasActionPerformed
        DateTimeFormatter localDateFormatter = DateTimeFormatter.ofPattern("dd/MM/uuuu");
        DateTimeFormatter localHourFormatter = DateTimeFormatter.ofPattern("HH:mm");

        String diaMin = diaMintxt.getText();
        String diaMax = diaMaxtxt.getText();
        String mesMin = mesMintxt.getText();
        String mesMax = mesMaxtxt.getText();
        String anoMin = anoMintxt.getText();
        String anoMax = anoMaxtxt.getText();

        if (diaMin.length() == 1) {
            diaMin = "0" + diaMin;
        }
        if (mesMin.length() == 1) {
            mesMin = "0" + mesMin;
        }
        if (diaMax.length() == 1) {
            diaMax = "0" + diaMax;
        }
        if (mesMax.length() == 1) {
            mesMax = "0" + mesMax;
        }

        String dataMin = anoMin + mesMin + diaMin;
        String dataMax = anoMax + mesMax + diaMax;

        boolean validaMax = ProxyColaborador.isDateValid(dataMin);
        boolean validaMin = ProxyColaborador.isDateValid(dataMax);

        if (validaMax == false || validaMin == false) {
            JOptionPane.showMessageDialog(null, "Intervalo de data inválido. Tente novamente!");
        } else {
            LocalDate dataMinima = LocalDate.parse(anoMin + "-" + mesMin + "-" + diaMin);
            LocalDate dataMaxima = LocalDate.parse(anoMax + "-" + mesMax + "-" + diaMax);
            ArrayList<Cliente> listaClientes = ProxyAdministrador.getClientes();

            float recTotal = 0;
            int qntCancelados = 0;
            int qntAceitos = 0;
            int qntEntregues = 0;
            String statusPedido = "";

            DefaultTableModel dtmVendas = (DefaultTableModel) relVendasTable.getModel();
            dtmVendas.setRowCount(0);

            for (int k = 0; k < listaClientes.size(); k++) {
                for (int j = 0; j < listaClientes.get(k).getPedidosCliente().size(); j++) {
                    LocalDate dataPedido = LocalDate.parse(listaClientes.get(k).getPedidosCliente().get(j).getDataPedido()); //passar o novo formater
                    LocalTime horaPedido = LocalTime.parse(listaClientes.get(k).getPedidosCliente().get(j).getHoraPedido());
                    if (((dataPedido.isEqual(dataMinima)) || (dataPedido.isAfter(dataMinima))) && ((dataPedido.isEqual(dataMaxima)) || (dataPedido.isBefore(dataMaxima)))) {

                        switch (listaClientes.get(k).getPedidosCliente().get(j).getStatusPedido()) {
                            case 1 -> {
                                statusPedido = "ACEITO";
                            }
                            case 2 -> {
                                statusPedido = "EM PREPARO";
                            }
                            case 3 -> {
                                statusPedido = "SAIU PARA ENTREGA";
                            }
                            case 4 -> {
                                qntEntregues++;
                                recTotal += listaClientes.get(k).getPedidosCliente().get(j).getValorTotalPedido();
                                statusPedido = "ENTREGUE";
                            }
                            case 5 -> {
                                qntCancelados++;
                                statusPedido = "CANCELADO";
                            }

                        }

                        qntAceitos++;

                        Object[] dados = {listaClientes.get(k).getCPF(),
                            listaClientes.get(k).getNomePessoa() + " " + listaClientes.get(k).getSobrenomePessoa(),
                            listaClientes.get(k).getPedidosCliente().get(j).getIdPedido(),
                            listaClientes.get(k).getPedidosCliente().get(j).getValorTotalPedido(),
                            dataPedido.format(localDateFormatter), horaPedido.format(localHourFormatter),
                            statusPedido};
                        dtmVendas.addRow(dados);
                    }
                }
            }
            
            receitaTotal.setText(Float.toString(recTotal));
            pedidosAceitos.setText(Integer.toString(qntAceitos));
            pedidosCancelados.setText(Integer.toString(qntCancelados));
            pedidosEntregues.setText(Integer.toString(qntEntregues));

        }


    }//GEN-LAST:event_gerarRelVendasActionPerformed

